    private void seteazaSarbatoriti() {
        for (Contact c : Agenda.getLstContact()) {
            if (c.getLunaNastere() == LocalDate.now().getMonthValue() && c.getZiNastere() == LocalDate.now().getDayOfMonth()) {
                model.addElement(c);
            }

        }
    }

